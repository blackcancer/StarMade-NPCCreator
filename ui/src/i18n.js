export function getAvailableLocales() {
  return SUPPORTED_LOCALES.slice();
}

export function getLocale() {
  try {
    const stored = globalThis.localStorage?.getItem(STORAGE_KEY);
    if (SUPPORTED_LOCALES.includes(stored)) return stored;
  } catch {}
  const navLang = globalThis.navigator?.language || globalThis.navigator?.userLanguage || 'en';
  const nav = String(navLang).toLowerCase();
  return nav.startsWith('fr') ? 'fr' : 'en';
}

export function setLocale(locale) {
  const next = SUPPORTED_LOCALES.includes(locale) ? locale : 'en';
  try { globalThis.localStorage?.setItem(STORAGE_KEY, next); } catch {}
}

export function hasTranslation(text, locale = getLocale()) {
  return !!DICTS[locale]?.[text];
}

export function t(text, vars = undefined) {
  const key = String(text ?? '');
  let out = DICTS[getLocale()]?.[key] || key;
  if (vars) {
    for (const [k, v] of Object.entries(vars)) out = out.replaceAll(`{${k}}`, String(v));
  }
  return out;
}

